<h2>POLITICA DE PRIVACIDAD</h2>

<p id="parrafo">En cumplimiento con lo establecido por la Ley Federal de Protección de Datos Personales en Posesión de los Particulares, publicada en el Diario Oficial de la Federación el día 5 de julio de 2010 (la "Ley"), su Reglamento y los Lineamientos del Aviso de Privacidad, publicados en el Diario Oficial de la Federación el día 17 de enero del 2013, se extiende el presente Aviso de Privacidad.
</p>

<br><br>

<h2>I. Responsable del Tratamiento de Datos Personales</h2>
<p id="parrafo">FERRE-PLUS, S.A. de C.V. ("FERRE-PLUS"), con domicilio en Parque Industrial No. 1, Jilotepec, Edo. de México, C.P. 54240, por medio del presente hace de su conocimiento que sus datos personales serán tratados estrictamente para los fines que más adelante se señalan.</p>

<br><br>

<h2>II. Datos Personales</h2>
<p id="parrafo">Los datos personales que serán tratados por "FERRE-PLUS" consisten en información personal del titular, la cual puede comprender: su nombre completo, dirección de correo electrónico, domicilio, números de teléfono, Registro Federal de Contribuyentes, entre otra estrictamente indispensable para los fines que posteriormente se mencionan.
<br><br>
En caso de no contar con sus datos personales, "FERRE-PLUS" no estará en posibilidad de usarlos, por lo que "FERRE-PLUS" no tendrá ningún tipo de responsabilidad derivada del manejo de los mismos.
</p>

<br><br>

<h2>III. Finalidades del Tratamiento de Datos Personales</h2>
<p id="parrafo">"FERRE-PLUS" podrá recabar sus datos personales sin fines de divulgación o utilización comercial, y será utilizada únicamente para los siguientes fines:
<br><br>
La realización de transacciones y actos a ser potencialmente celebrados con "FERRE-PLUS".
La realización de encuestas de cualquier tipo.
El trato de cualquier asunto derivado de la relación que pueda tener con "FERRE-PLUS", o con cualquiera de sus filiales.
La promoción de productos, descuentos, promociones, bonificaciones, concursos y publicidad en general, ofrecidos por "FERRE-PLUS", o por cualquiera de sus filiales.
Las revisiones y correcciones requeridas para la actualización tanto de datos personales como de este sitio web para mayor conocimiento de los titulares, tanto para efectos legales como de seguridad.
La comunicación con sus clientes, proveedores, empleados o cualquier persona para tratar cualquier tema relacionado con sus datos personales o con el presente Aviso de Privacidad.
El trato de cualquier tema relacionado con la información de este sitio web.
La propalación de información que "FERRE-PLUS" considere de interés.
Cualquier otra actividad necesaria para la realización de los fines antes mencionados.
Los datos personales serán guardados bajo la más estricta confidencialidad y no se les podrá dar un uso distinto a los antes mencionados, salvo que medie un cambio en este Aviso de Privacidad, de conformidad con lo previsto en el apartado XI del presente documento.
<br><br>
Los datos personales, una vez que se cumpla la finalidad del tratamiento de los mismos, serán bloqueados con el único propósito de determinar posibles responsabilidades en relación con su tratamiento, hasta el plazo de prescripción legal o contractual aplicable. Durante dicho periodo, los datos personales no podrán ser objeto de tratamiento y, transcurrido éste, se procederá a su cancelación en la base de datos correspondiente y, en su caso, a la destrucción de los registros físicos.
</p>


<br><br>

<h2>V. Opciones y medios para limitar el uso o divulgación de los datos personales</h2>
<p id="parrafo">En "FERRE-PLUS" se cuenta con las medidas de seguridad, administrativas, técnicas y físicas necesarias y suficientes para proteger sus datos personales contra daño, pérdida, alteración, destrucción, uso, acceso o tratamiento no autorizado.
<br><br>
Los datos personales son salvaguardados en bases de datos y equipos de cómputo que cuentan con la seguridad necesaria para prevenir fugas de información. Controles de acceso físico y lógico, controles ambientales, sistemas de protección anti intrusos (IPS, Firewall), herramientas de protección antivirus y filtrado web, son algunas de las herramientas utilizadas para mantener la seguridad de los datos en los sistemas de información de "FERRE-PLUS".
<br><br>
Las herramientas de seguridad informática mencionadas en el párrafo anterior están apoyadas por una política interna de seguridad de la información que explica a los empleados las consideraciones de seguridad que deben tomar en cuenta al utilizar un sistema informático, misma que es reforzada constantemente.
<br><br>
Por ello, se hacen de su conocimiento las opciones que usted tiene para solicitar la limitación del uso o divulgación de sus datos personales, materia de este Aviso de Privacidad: por escrito enviado al Encargado del Tratamiento de Datos Personales (en el domicilio señalado en el punto VI siguiente); o vía correo electrónico dirigido a la dirección datospersonales@FERRE-PLUS.com.

</p>


