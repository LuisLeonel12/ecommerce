<h2>POLÍTICA DE DEVOLUCIONES DE FERRE-PLUS</h2>

<p id="parrafo">A menos que se indique lo contrario en esta Política, los productos enviados por FERRE-PLUS, incluidos los productos de Remates de almacén, pueden devolverse dentro de los 30 días posteriores a la fecha de entrega en la mayoría de los casos. Los productos se deberán devolver en el mismo estado en el que se recibieron. Por lo tanto, los productos nuevos se deben devolver nuevos y completos. Algunos productos tienen diferentes políticas o requisitos. Cuando devuelves un producto, aceptas el reembolso y la forma en que este se emite pueden variar de acuerdo con el estado del producto, el tiempo que lo tuviste y la forma en que se compró. Si devuelves un producto diferente o uno que esté en un estado diferente a como lo enviamos, no podremos procesar tu reembolso.
</p>

<br><br>

<h2>Hay dos opciones para devolver un producto comprado en FERRE-PLUS.com.mx gestionado por FERRE-PLUS:</h2>
<p id="parrafo">
Si la dirección de devolución no está cubierta por nuestro transportista, puedes elegir un transportista de tu elección y un método de envío seguro con rastreo. Deberás pagar la devolución y una vez que hayamos recibido el producto, puedes enviar un correo electrónico a Servicio al cliente de FERRE-PLUS México, adjuntando el recibo de los costos de envío de devolución para recibir un reembolso.
</p>

<p id="parrafo">
Si el motivo de la devolución del producto no se debe a que se trata de un producto dañado, defectuoso o incorrecto, no reembolsaremos el valor de la envoltura para regalo, los gastos de envío o cualquier otro servicio relacionado con tu pedido; solo reembolsaremos el valor del producto.
</p>
<br><br>
<p id="parrafo">
Nota: Al devolver un producto, debes devolverlo en el mismo estado en el que lo recibiste de FERRE-PLUS. Debes eliminar toda la información personal acerca de ti o de un tercero que pueda estar presente en ese producto, ya que FERRE-PLUS no será responsable de salvaguardar esta información. Del mismo modo, FERRE-PLUS no será responsable de los productos que dejes por error en el producto devuelto o dentro del paquete.
</p>

<br><br>

<h2>devoluciones equivocadas</h2>
<p id="parrafo">
Asegúrate de incluir el producto correcto y completo cuando realices una devolución. Se tiene que devolver el producto correcto para recibir un reembolso.
</p>

<p id="parrafo">
Nota: Si FERRE-PLUS.com.mx te envía un reemplazo por un producto dañado o defectuoso, debes devolver el producto original dentro de los 30 días posteriores a la fecha en que confirmamos el pedido de reemplazo. Si no nos devuelves el producto original dentro de este plazo de 30 días, FERRE-PLUS se reserva el derecho de hacer un cargo por el precio del producto de reemplazo en el mismo método de pago que utilizaste para confirmar el pedido.
</p>

<br><br>

<h2> Costo a cargo del Usuario vendedor</h2>
<p id="parrafo">"El Usuario vendedor podrá ofrecer en sus publicaciones la opción de “Envío gratis”. Al seleccionar esta opción se le informarán los costos estimados para la entrega del producto usando la Herramienta de Mercado Envíos. El Usuario vendedor se hará cargo de todos los costos por el uso de la Herramienta de FERRE-PLUS en la modalidad de “Envío gratis”, respetando la totalidad de las cláusulas incluidas en los Términos y Condiciones de FERRE-PLUS y demás Términos y Condiciones Generales de FERRE-PLUS.

FERRE-PLUS podrá establecer que ciertos Usuarios vendedores ofrezcan la Herramienta de FERRE-PLUS con costo a su cargo para sus publicaciones por precios iguales o mayores a un monto que FERRE-PLUS estipule y/o, en su caso, FERRE-PLUS podrá decidir subsidiar los costos de envío. Conocer más sobre los costos de envío y eventuales subsidios.
</p>

<br><br>

<h2>Productos dañados</h2>
<p id="parrafo">
Si recibes un producto dañado, visita nuestro Centro de devoluciones para regresarlo dentro de los 30 días siguientes a la fecha de recepción del pedido o ponte en contacto con nuestro departamento de Servicio al cliente.
</p>

<p id="parrafo">
Nota: Para los productos defectuosos o dañados comprados y enviados por vendedores en FERRE-PLUS.com.mx, ponte en contacto con el vendedor. No devuelvas estos productos directamente a FERRE-PLUS.
</p>

<p id="parrafo">
Para la mayoría de los productos de FERRE-PLUS Internacional, se te proporcionará una etiqueta de devolución prepagada de un transportista, que te permite devolver los productos al punto de entrega de cualquier transportista.

Sin embargo, ten en cuenta que no todos los productos se pueden devolver con una etiqueta de devolución prepagada, en cuyo caso deberás devolverlo por tu cuenta con la etiqueta de devolución. FERRE-PLUS te reembolsará automáticamente hasta MXN$400 por costo de envío de devolución (para productos vendidos por FERRE-PLUS Estados Unidos). Si el costo de envío de devolución es superior al monto del reembolso automático que recibiste, guarda una copia del recibo.
</p>

